package de.heaal.eaf.base;

/**
 * A generic individual that can be used in various EA algorithms.
 * 
 * @author Christian Lins <christian.lins@haw-hamburg.de>
 */
public class GenericIndividual extends AbstractIndividual {

    
    public GenericIndividual(VecN genome) {
        this.genome = genome;
    }   
    
    @Override
    public Individual copy() {
        return new GenericIndividual(genome.copy());
    }

}
